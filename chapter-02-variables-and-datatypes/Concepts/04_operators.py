# Arithmetic Operators
a = 7
b = 10
c = a + b
print(c)


# Assignment Operators
a = 4 - 2
print(a)

b = 6

b += 3  # Increment the value of b by 3
b -= 3  # Decrement the value of b by 3
b *= 3  # Multiply the value of b by 3
b /= 3  # Divide the value of b by 3

print(b)


# Comparison Operators
d = 5 != 6
print(d)


# Logical Operators
e = True or False
print(e)

print(not (True))