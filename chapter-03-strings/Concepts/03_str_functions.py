name = "anshulika nagariya"

print(len(name))    # Returns the length of the string

print(name.lower())    # Converts all characters to lowercase

print(name.upper())    # Converts all characters to uppercase

print(name.endswith("iya"))    # Checks if the string ends with "iya"

print(name.startswith("Yas"))    # Checks if the string starts with "Yas"

print(name.capitalize())    # Capitalizes the first letter of the string

print(name.title())    # Capitalizes the first letter of each word

print(name.strip())    # Removes extra spaces from the beginning and end

print(name.count("a"))    # Counts how many times "a" appears in the string

print(name.find("a"))    # Finds the index of the first occurrence of "a"

print(name.replace("anshulika", "Anshulika"))    # Replaces "anshulika" with "Anshulika"

print(name.split())    # Splits the string into a list of words

print(name.join(["1.", "2.", "3."]))    # Joins the list using the string as a separator